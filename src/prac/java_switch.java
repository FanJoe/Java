package prac;

public class java_switch {
	
	public static void main(String[] args) {
		String ch = "c";
		
		switch(ch)
		{
			case "a":
				System.out.println("AAAA");
				break;
			case "b":
				System.out.println("BBBB");
				break;
			case "c":
				System.out.println("CCCC");
				break;
			default:
				System.out.println("default");
					
		}
	}

}
